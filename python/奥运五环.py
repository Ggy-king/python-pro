#奥运五环
import turtle
turtle.seth(90)
turtle.pensize(3)
turtle.pencolor("blue")
turtle.circle(40,360)
turtle.pencolor("black")
turtle.seth(-90)
turtle.circle(40,360)
turtle.seth(0)
turtle.up()
turtle.fd(80)
turtle.down()
turtle.pencolor("red")
turtle.seth(-90)
turtle.circle(40,360)
turtle.seth(0)
turtle.pencolor("green")
turtle.circle(-40,360)
turtle.up()
turtle.goto(0,0)
turtle.down()
turtle.pencolor("yellow")
turtle.circle(-40,360)
